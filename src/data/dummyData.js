// 案件名を自動生成するヘルパー
export function generateCaseName(customerName, location, propertyType, side) {
  const parts = [];
  if (customerName) parts.push(`${customerName}様`);
  if (location) parts.push(location);
  if (propertyType) parts.push(propertyType);
  parts.push(side === '売主側' ? '売却' : '購入');
  return parts.join(' ');
}

